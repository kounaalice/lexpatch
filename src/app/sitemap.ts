export default function sitemap(): MetadataRoute.Sitemap {
  const now = new Date().toISOString();

  // Static pages
  const staticPages: MetadataRoute.Sitemap = [
    { url: BASE, lastModified: now, changeFrequency: "daily", priority: 1.0 },
    { url: `${BASE}/search`, lastModified: now, changeFrequency: "daily", priority: 0.9 },
    { url: `${BASE}/patches`, lastModified: now, changeFrequency: "daily", priority: 0.8 },
    { url: `${BASE}/commentaries`, lastModified: now, changeFrequency: "daily", priority: 0.8 },
    { url: `${BASE}/projects`, lastModified: now, changeFrequency: "daily", priority: 0.8 },
    { url: `${BASE}/news`, lastModified: now, changeFrequency: "weekly", priority: 0.7 },
    { url: `${BASE}/communities`, lastModified: now, changeFrequency: "weekly", priority: 0.7 },
    { url: `${BASE}/guide`, lastModified: now, changeFrequency: "monthly", priority: 0.7 },
    { url: `${BASE}/about`, lastModified: now, changeFrequency: "monthly", priority: 0.7 },
    { url: `${BASE}/prefectures`, lastModified: now, changeFrequency: "monthly", priority: 0.6 },
    { url: `${BASE}/contact`, lastModified: now, changeFrequency: "monthly", priority: 0.5 },
    { url: `${BASE}/legal`, lastModified: now, changeFrequency: "yearly", priority: 0.3 },
    { url: `${BASE}/en`, lastModified: now, changeFrequency: "monthly", priority: 0.6 },
    { url: `${BASE}/cards`, lastModified: now, changeFrequency: "weekly", priority: 0.6 },
    { url: `${BASE}/calendar`, lastModified: now, changeFrequency: "daily", priority: 0.6 },
    { url: `${BASE}/stats`, lastModified: now, changeFrequency: "weekly", priority: 0.4 },
  ];

  // 106 law pages (high priority — core content)
  const lawPages: MetadataRoute.Sitemap = STATIC_LAW_IDS.map((id) => ({
    url: `${BASE}/law/${id}`,
    lastModified: now,
    changeFrequency: "weekly" as const,
    priority: 0.8,
  }));

  // 92 category pages
  const categoryPages: MetadataRoute.Sitemap = LAW_CATEGORIES.map((c) => ({
    url: `${BASE}/category/${c.slug}`,
    lastModified: now,
    changeFrequency: "monthly" as const,
    priority: 0.6,
  }));

  return [...staticPages, ...lawPages, ...categoryPages];
}
