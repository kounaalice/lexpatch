// ── ヘルパー関数 ──

export function getFieldByCode(code: string): W100Field | undefined {
  return W100_FIELDS.find((f) => f.code === code);
}

export function getFieldGroup(groupId: string): W100FieldGroup | undefined {
  return W100_FIELD_GROUPS.find((g) => g.id === groupId);
}

export function getFieldsByGroup(groupId: string): W100Field[] {
  return W100_FIELDS.filter((f) => f.groupId === groupId);
}

export function getAspectByCode(code: string): W100Aspect | undefined {
  return W100_ASPECTS.find((a) => a.code === code);
}

export function getTTGroupByCode(ttCode: string): W100TopicGroup | undefined {
  const num = parseInt(ttCode, 10);
  const rangeStart = Math.floor(num / 10) * 10;
  const range = `${String(rangeStart).padStart(2, "0")}-${String(rangeStart + 9).padStart(2, "0")}`;
  return W100_TT_GROUPS.find((g) => g.range === range);
}

/** CCコード → グループの色情報を取得 */
export function getFieldColor(ccCode: string): { color: string; bg: string } {
  const field = getFieldByCode(ccCode);
  if (!field) return { color: "#374151", bg: "#F3F4F6" };
  const group = getFieldGroup(field.groupId);
  if (!group) return { color: "#374151", bg: "#F3F4F6" };
  return { color: group.color, bg: group.bg };
}

